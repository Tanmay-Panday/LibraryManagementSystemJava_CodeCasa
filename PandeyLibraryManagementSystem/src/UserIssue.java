    private boolean validateCredential() {
        boolean b=false;
        String stdID=sid_intTXT.getText();
        int Istdid=Integer.parseInt(stdID);
        String stdpw=passTXT.getText();
        try {
            Connection con=ConnectionOP.getConnection();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from useridpw where id="+Istdid+" and password='"+stdpw+"'");
            if(rs.next()) {
                b=true;
                clrscr();
                Std_okLBL.setVisible(true);
                sid_intTXT.setEditable(false);
                passTXT.setEditable(false);
                bid_txt.setEditable(true);
                
            }
            else {
                JOptionPane.showMessageDialog(null, "INCORRECT USER ID OR PASSWORD \n PLEASE TRY AGAIN");
                Std_notokLBL1.setVisible(true);
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null,"error in validating credintials");
        }
            return b;
    }
    // to check wether book is available or not
    private void validateBookAvailablity() {
        String bid=bid_txt.getText();
        try{
            Connection con=ConnectionOP.getConnection();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select availability from bookinfo where bookid='"+bid+"'");
            if(rs.next()) {
                String ctr=rs.getString(1);
                if(ctr.equals("YES")) {
                    clrbookvalid();
                    B_okLBL.setVisible(true);
                    bid_txt.setEditable(false);
                }
                else {
                    JOptionPane.showMessageDialog(null, "id is correct but book is not available");
                    B_notokLBL.setVisible(true);
                   }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "enter correct book id");
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "error in validating book availability");
        }
    }
