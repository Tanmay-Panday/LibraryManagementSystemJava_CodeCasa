    // function to issue book
    private void issue() {
        java.util.Date aIssue=idate.getDatoFecha();
        java.util.Date aDue=ddate.getDatoFecha();
        Long l1=aIssue.getTime();
        Long l2=aDue.getTime();
        
        java.sql.Date sIssue= new java.sql.Date(l1);
        java.sql.Date sDue= new java.sql.Date(l2);
        int sid=Integer.parseInt(sid_intTXT.getText());  // student id
        String bid=bid_txt.getText(); // book id
        
        try {
            Connection con=ConnectionOP.getConnection();
            PreparedStatement pst=con.prepareStatement("insert into issuereturn (studentid,bookid,issue_date,due_date) values(?,?,?,?)");
            pst.setInt(1, sid);
            pst.setString(2, bid);
            pst.setDate(3, sIssue);
            pst.setDate(4, sDue);
            
            int affrow = pst.executeUpdate();
            
            if(affrow!=0) {
                JOptionPane.showMessageDialog(null, "CONGRATULATIONS YOU HAVE SUCCESSFULLY ISSUED A BOOK");
                NOTAVAILABLE(bid);
                clreverything();
            }
            else {
                JOptionPane.showMessageDialog(null, "NOT ISSUED");
                clreverything();
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "book already issued by you");
        }
        
    }
    //function to change availability in database
    private void NOTAVAILABLE(String bid) {
        try {
            Connection con=ConnectionOP.getConnection();
            PreparedStatement pst=con.prepareStatement("update bookinfo set availability='NO' where bookid=?");
            pst.setString(1,bid);
            int affrow=pst.executeUpdate();
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "error in updating availability");
        }
    }
